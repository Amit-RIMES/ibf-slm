def get_translations(lang: str) -> dict[str, str]:
    return _TRANSLATIONS.get(lang, _TRANSLATIONS["en"])


def build_drought_status(T: dict[str, str], ts: int, spi_val: float,
                         label: str, month_name: str, year: int) -> str:
    if T.get("lang_name") == "English":
        return (
            f"SPI-{ts} indicates {label.lower()} conditions "
            f"({spi_val:+.2f}) as of {month_name} {year}."
        )
    return (
        f"SPI-{ts} {T['drought_indicates']} {label.lower()} {T['drought_conditions']} "
        f"({spi_val:+.2f}) {T['drought_as_of']} {month_name} {year}."
    ).strip().replace("  ", " ")


def build_impact_summary(T: dict[str, str], n_impacts: int, days: int,
                         total_affected: int) -> str:
    event_word = T["impact_event"] if n_impacts == 1 else T["impact_events"]
    base = f"{n_impacts} {event_word} {T['recorded_past']} {days} {T['days_label']}"
    if total_affected:
        base = base.rstrip(".") + f", {T['affecting']} {total_affected:,} {T['people']}."
    return base
